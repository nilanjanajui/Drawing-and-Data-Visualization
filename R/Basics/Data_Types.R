## A variable can store different types of values such as numbers, characters etc. 
## We can print data type using "class"
## These different types of data that we can use in our code are called data types. For example,

x <- 123L # Integer constants are the integer values we use in our code. 
# These constants end with the letter L.

# print value of x
print(x)

# print type of x
print(class(x))


## 1. Logical(boolean) Data type
bool1 <- TRUE
print(bool1)
print(class(bool1))

bool2 <- FALSE
print(bool2)
print(class(bool2))


## 2. Numeric Data type
# floating point values
weight <- 63.5
print(weight)
print(class(weight))

# real numbers
height <- 182
print(height)
print(class(height))


## 3. Character Data type
# create a string variable
fruit <- "Apple"
print(class(fruit))

# create a character variable
my_char <- 'A'
print(class(my_char))


## 4. Integer Data Type
# The integer data type specifies real values without decimal points. 
# We use the suffix L to specify integer data. For example,
integer_variable <- 186L
print(class(integer_variable))


## 5. Complex Data Type
# The complex data type is used to specify purely imaginary values in R. 
# We use the suffix i to specify the imaginary part. For example,
# 2i represents imaginary part
complex_value <- 3 + 2i

# print class of complex_value
print(class(complex_value))









