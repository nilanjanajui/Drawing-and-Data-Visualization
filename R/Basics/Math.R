## Built-in Math Functions
# R also has many built-in math functions that allows us to perform mathematical tasks on numbers.

## min & max
max(5, 10, 15)
min(5, 10, 15)


## sqrt()
sqrt(16)


## abs()
abs(-4.7)   # absolute(Positive) value of numbers


## ceiling() and floor()
ceiling(1.4)  # rounds a number upwards to its nearest integer,
floor(1.4)    # rounds a number downwards to its nearest integer
